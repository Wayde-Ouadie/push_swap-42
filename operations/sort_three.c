#include "../push_swap.h"

void	sort_three(t_stack **a)
{
	t_stack	*max;

	max = max_stack(*a);
	if (*a == max)
		ra(a);
	else if ((*a)->next == max)
		rra(a);
	if ((*a)->value > (*a)->next->value)
		sa(a);
}